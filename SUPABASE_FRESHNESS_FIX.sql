--  뽑뽑 신선도 보정
--  증상: 오래된(5월 등) 제보가 "최근 확인"으로 표시됨
--  원인: last_verified_at 컬럼을 default now()로 추가해서
--        옛날 제보의 확인 시각이 "컬럼 생성 시점(최근)"으로 채워짐
--  해결: 아직 아무도 재인증 안 한 제보는 last_verified_at을
--        원래 등록일(created_at)로 되돌린다.
--  ⚠️ Supabase SQL Editor에서 실행. 1회성.
-- ============================================================

-- 재인증 기록(verify_count)이 0인 = 아무도 확인 안 한 제보만 보정
-- (누가 실제로 재인증한 제보는 그 시각이 맞으므로 건드리지 않음)
update posts
set last_verified_at = created_at
where coalesce(verify_count, 0) = 0
  and created_at is not null;

